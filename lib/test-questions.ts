export interface TestResult {
  primary: Track;
  scores: Record<Track, number>;
  mixed: boolean;
}

export function scoreAnswers(picked: Record<number, string>): TestResult {
  const scores: Record<Track, number> = { V: 0, P: 0, A: 0 };
  for (const q of questions) {
    const ans = picked[q.id];
    const option = q.options.find((o) => o.id === ans);
    if (option) scores[option.track] += 1;
  }
  const entries = Object.entries(scores) as [Track, number][];
  entries.sort((a, b) => b[1] - a[1]);
  const [primary, primaryScore] = entries[0];
  const [, secondScore] = entries[1];
  const mixed = primaryScore - secondScore <= 1;
  return { primary, scores, mixed };
}

